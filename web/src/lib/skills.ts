export function simulateContentReviewer(
  req: ContentReviewerRequest
): ReviewResult {
  const content = req.content;
  const stages: ReviewResult["stages"] = [];
  const now = new Date().toISOString();

  // Stage 1: Format & Sensitive Words
  const stage1Issues: ReviewResult["stages"][0]["issues"] = [];

  const absoluteWords = ["最好", "最佳", "最先进", "第一", "唯一", "100%", "绝对"];
  for (const word of absoluteWords) {
    if (content.includes(word)) {
      stage1Issues.push({
        level: "warning",
        category: "绝对化用语",
        text: `内容中包含绝对化用语"${word}"`,
        suggestion: `建议替换为更客观的表述，避免使用"${word}"`,
        location: `包含"${word}"的段落`,
      });
    }
  }

  const sensitiveWords = ["暴力执法", "以罚代管", "最危险", "必死"];
  for (const word of sensitiveWords) {
    if (content.includes(word)) {
      stage1Issues.push({
        level: "block",
        category: "敏感词",
        text: `内容中包含敏感词"${word}"`,
        suggestion: `请删除或替换"${word}"`,
        location: `包含"${word}"的段落`,
      });
    }
  }

  const stage1Result =
    stage1Issues.some((i) => i.level === "block")
      ? "block"
      : stage1Issues.length > 0
        ? "warning"
        : "pass";

  stages.push({
    stage: "ai_first_review",
    stageName: "初审：格式与敏感词",
    result: stage1Result,
    issues: stage1Issues,
    timestamp: now,
  });

  // Stage 2: Content Quality
  const stage2Issues: ReviewResult["stages"][0]["issues"] = [];

  if (content.length < 50) {
    stage2Issues.push({
      level: "warning",
      category: "内容长度",
      text: "内容过短，可能信息不够充分",
      suggestion: "建议补充更多细节和信息",
      location: "全文",
    });
  }

  const dataPattern = /据统计|数据显示|研究表明/;
  if (dataPattern.test(content) && !content.includes("来源")) {
    stage2Issues.push({
      level: "warning",
      category: "数据引用",
      text: "引用了统计数据但未标注来源",
      suggestion: "请为引用的数据添加可靠来源",
      location: "数据引用段落",
    });
  }

  const stage2Result =
    stage2Issues.some((i) => i.level === "block")
      ? "block"
      : stage2Issues.length > 0
        ? "warning"
        : "pass";

  stages.push({
    stage: "ai_quality_review",
    stageName: "复审：内容质量",
    result: stage2Result,
    issues: stage2Issues,
    timestamp: now,
  });

  // Stage 3: Compliance
  const stage3Issues: ReviewResult["stages"][0]["issues"] = [];

  const namePattern = /[张李王赵刘陈][某某某]|[A-Z]\d{5}/;
  if (namePattern.test(content)) {
    stage3Issues.push({
      level: "warning",
      category: "隐私保护",
      text: "内容中可能包含未完全脱敏的个人信息",
      suggestion: "请确认所有个人信息已做脱敏处理",
      location: "涉及个人信息的段落",
    });
  }

  const stage3Result =
    stage3Issues.some((i) => i.level === "block")
      ? "block"
      : stage3Issues.length > 0
        ? "warning"
        : "pass";

  stages.push({
    stage: "ai_compliance_review",
    stageName: "终审：政策合规",
    result: stage3Result,
    issues: stage3Issues,
    timestamp: now,
  });

  const hasBlock = stages.some((s) => s.result === "block");
  const hasWarning = stages.some((s) => s.result === "warning");

  return {
    id: `review-${Date.now()}`,
    contentFile: "用户输入内容",
    stages,
    finalResult: hasBlock
      ? "rejected"
      : hasWarning
        ? "revision_required"
        : "approved",
    summary: hasBlock
      ? "内容存在必须修改的问题，请修改后重新提交审核"
      : hasWarning
        ? "内容基本合格，但存在需要注意的问题，建议修改后发布"
        : "内容审核通过，可以发布",
  };
}

